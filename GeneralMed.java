    private void genSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_genSaveButtonActionPerformed
        // TODO add your handling code here:
        
        int genID = Integer.parseInt(generalID.getText());
        
        String tobacco = tobaccoField.getText();
        String tQuan = tobaccoQuantityField.getText();
        String tDur = tobaccoDurationField.getText();
        String alcohol = alcoholField.getText();
        String aQuan = alcoholQuantityField.getText();
        String aDur = alcoholDurationField.getText();
        String drug = drugsField.getText();
        String dType = drugTypeField.getText();
        String dDur = drugsDurationField.getText();
        String btype = bloodTypeField.getText();
        String rh = rhFactorField.getText();
        int dele;
        
        if (outdated.isSelected()) {
            dele = 1;
        }
        else {
            dele = 0;
        }
        
        try {
            con = connections.getConnection();
                
            //set up prepared update query
            pst = con.prepareStatement("UPDATE generalmedicalhistorytable SET Tobacco=?, TobaccoQuantity=?, "
                + "Tobaccoduration=?, Alcohol=?, AlcoholQuantity=?, Alcoholduration=?, Drug=?, "
                + "DrugType=?, Drugduration=?, BloodType=?, Rh=?, deleted=? WHERE GeneralMedicalHistoryID=?");
                
            //set values for update query based on text box data inputted
            pst.setString(1, tobacco);
            pst.setString(2, tQuan);
            pst.setString(3, tDur);
            pst.setString(4, alcohol);
            pst.setString(5, aQuan);
            pst.setString(6, aDur);
            pst.setString(7, drug);
            pst.setString(8, dType);
            pst.setString(9, dDur);
            pst.setString(10, btype);
            pst.setString(11, rh);
            pst.setInt(12, dele);
            
            pst.setObject(13, genID);
            //execute query
            pst.execute();
                
            //terminal at bottom
            java.util.Date date = new java.util.Date();
            System.out.println("Saved General Med History successfully! :D");
            console += date + ": Saved General Med History successfully! :D\n";
            generalEvents.setText(console);
            
        } catch (Exception ex) {
            //terminal at bottom
            java.util.Date date = new java.util.Date();
            console += date + ": Failed to save General Med History :<\n";
            System.out.println("Failed to save General Med History :<");
            generalEvents.setText(console);
        }
        
        
        genSaveButton.setEnabled(false);
        genDeleteButton.setEnabled(false);
        outdated.setEnabled(false);
        genEditButton.setEnabled(true);
        
        tobaccoField.setEnabled(false);
        tobaccoDurationField.setEnabled(false);
        tobaccoQuantityField.setEnabled(false);
        alcoholField.setEnabled(false);
        alcoholDurationField.setEnabled(false);
        alcoholQuantityField.setEnabled(false);
        drugsField.setEnabled(false);
        drugsDurationField.setEnabled(false);
        drugTypeField.setEnabled(false);
        bloodTypeField.setEnabled(false);
        rhFactorField.setEnabled(false);
        
        
    }//GEN-LAST:event_genSaveButtonActionPerformed

