    private void loadHistoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadHistoryActionPerformed
        // TODO add your handling code here:
        
        DefaultListModel general = new DefaultListModel();
        try {
            
            //grab all patients from database
            con = connections.getConnection();
            pst = con.prepareStatement("SELECT * from generalmedicalhistorytable WHERE PatientID=?;");
            pst.setObject(1, Integer.parseInt(IDpatient.getText()));
            rs = pst.executeQuery();
            
            while(rs.next()) {
                int gID = rs.getInt("GeneralMedicalHistoryID");
                String fullString = gID + "  ---  " + IDpatient.getText();
                general.addElement(fullString);
            }
            generalList.setModel(general);
            
            java.util.Date date = new java.util.Date();
            console += date + ": General List Loaded Successfully! :D\n";
            generalEvents.setText(console);
        }
        catch(Exception e) {
            
            System.out.println("Could not connect successfully! :<");
            java.util.Date date = new java.util.Date();
            console += date + ": Failed to connect :(\n";
            generalEvents.setText(console);
        }
        
    }//GEN-LAST:event_loadHistoryActionPerformed

